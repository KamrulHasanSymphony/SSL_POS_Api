    [Route("api/[controller]")]
    [ApiController]
    public class CollectionController : Controller
    {
        CollectionService _service = new CollectionService();
        CommonService _common = new CommonService();
        [HttpPost("Insert")]
        public async Task<ResultVM> Insert(CollectionVM model)
        {
            ResultVM resultVM = new ResultVM { Status = "Fail", Message = "Error", ExMessage = null, Id = "0", DataVM = null };
            _service = new CollectionService();

            try
            {
                resultVM = await _service.Insert(model);
                return resultVM;
            }
            catch (Exception ex)
            {
                return new ResultVM
                {
                    Status = "Fail",
                    Message = ex.Message,
                    ExMessage = ex.Message,
                    DataVM = model
                };
            }
        }

        // POST: api/Collection/Update
