        public async Task<ResultVM> Insert(PaymentTypeVM paymentType)
        {
            string CodeGroup = "PaymentType";
            string CodeName = "PaymentType";
            _commonRepo = new CommonRepository();
            PaymentTypeRepository _repo = new PaymentTypeRepository();

            ResultVM result = new ResultVM { Status = "Fail", Message = "Error", ExMessage = null, Id = "0", DataVM = null };

            bool isNewConnection = false;
            SqlConnection conn = null;
            SqlTransaction transaction = null;
            try
            {
                conn = new SqlConnection(DatabaseHelper.GetConnectionString());
                conn.Open();
                isNewConnection = true;

                transaction = conn.BeginTransaction();
                #region Check Exist Data
                string[] conditionField = { "Name", "IsActive" };
                string[] conditionValue = { paymentType.Name.Trim(),"1" };

                bool exist = _commonRepo.CheckExists("PaymentTypes", conditionField, conditionValue, conn, transaction);

                if (exist)
                {
                    result.Message = "Data Already Exist!";
                    throw new Exception("Data Already Exist!");
                }
                #endregion
                result = await _repo.Insert(paymentType, conn, transaction);

                if (isNewConnection)
                {
                    transaction.Commit();
                }

                return result;
            }
            catch (Exception ex)
            {
                if (transaction != null && isNewConnection)
                {
                    transaction.Rollback();
                }

                result.ExMessage = ex.ToString();
                return result;
            }
            finally
            {
                if (isNewConnection && conn != null)
                {
                    conn.Close();
                }
            }
        }

