        public async Task<ResultVM> Insert(CustomerPaymentCollectionVM CustomerPaymentCollection)
        {
            string CodeGroup = "CustomerPaymentCollection";
            string CodeName = "CustomerPaymentCollection";
            CommonRepository _commonRepo = new CommonRepository();
            CustomerPaymentCollectionRepository _repo = new CustomerPaymentCollectionRepository();

            ResultVM result = new ResultVM { Status = "Fail", Message = "Error", ExMessage = null, Id = "0", DataVM = null };

            bool isNewConnection = false;
            SqlConnection conn = null;
            SqlTransaction transaction = null;
            try
            {
                conn = new SqlConnection(DatabaseHelper.GetConnectionString());
                conn.Open();
                isNewConnection = true;

                transaction = conn.BeginTransaction();
                string code = _commonRepo.CodeGenerationNo(CodeGroup, CodeName, conn, transaction);
          

                if (!string.IsNullOrEmpty(code))
                {
                    CustomerPaymentCollection.Code = code;

                    result = await _repo.Insert(CustomerPaymentCollection, conn, transaction);

                    if (isNewConnection)
                    {
                        transaction.Commit();
                    }

                    return result;
                }
                else
                {
                    throw new Exception("Code Generation Failed!");
                }

                //if (isNewConnection)
                //{
                //    transaction.Commit();
                //}
                //return result;


            }
            catch (Exception ex)
            {
                if (transaction != null && isNewConnection)
                {
                    transaction.Rollback();
                }

                result.ExMessage = ex.ToString();
                return result;
            }
            finally
            {
                if (isNewConnection && conn != null)
                {
                    conn.Close();
                }
            }
        }
       
