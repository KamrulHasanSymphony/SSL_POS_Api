        public async Task<ResultVM> Update(SaleReturnVM sale)
        {
            SaleReturnRepository _repo = new SaleReturnRepository();
            ResultVM result = new ResultVM { Status = "Fail", Message = "Error", ExMessage = null, Id = "0", DataVM = null };
            _commonRepo = new CommonRepository();
            bool isNewConnection = false;
            SqlConnection conn = null;
            SqlTransaction transaction = null;
            try
            {
                conn = new SqlConnection(DatabaseHelper.GetConnectionString());
                conn.Open();
                isNewConnection = true;

                transaction = conn.BeginTransaction();

                var record = _commonRepo.DetailsDelete("SaleReturnDetails", new[] { "SaleReturnId" }, new[] { sale.Id.ToString() }, conn, transaction);

                if (record.Status == "Fail")
                {
                    throw new Exception("Error in Delete for Details Data.");
                }

                result = await _repo.Update(sale, conn, transaction);

                if (sale.saleReturnDetailList == null || !sale.saleReturnDetailList.Any())
                {
                    throw new Exception("Sale Return Details is empty or null.");
                }

                if (result.Status.ToLower() == "success")
                {
                    int LineNo = 1;

                    decimal subtotal = 0;



                    foreach (var details in sale.saleReturnDetailList)
                    {
                        details.SaleReturnId = sale.Id;
                        details.SDAmount = 0;
                        details.VATAmount = 0;
                        //details.BranchId = sale.BranchId;
                        details.Line = LineNo;


                        #region Line Total Summation
                        if (details.SD > 0)
                        {
                            details.SDAmount = (details.SubTotal * details.SD) / 100;
                        }
                        if (details.VATRate > 0)
                        {
                            details.VATAmount = ((details.SubTotal + details.SDAmount) * details.VATRate) / 100;
                            // details.VATAmount = (details.SubTotal * details.VATRate) / 100;
                        }

                        details.LineTotal = details.SubTotal + details.SDAmount + details.VATAmount;

                        #endregion

                        var resultDetail = await _repo.InsertDetails(details, conn, transaction);

                        if (resultDetail.Status.ToLower() == "success")
                        {
                            LineNo++;
                        }
                        else
                        {
                            throw new Exception(resultDetail.Message);
                        }
                    }
                }
                else
                {
                    throw new Exception(result.Message);
                }

                if (isNewConnection && result.Status == "Success")
                {
                    transaction.Commit();
                }
                else
                {
                    throw new Exception(result.Message);
                }

                return result;
            }
            catch (Exception ex)
            {
                if (transaction != null && isNewConnection)
                {
                    transaction.Rollback();
                }
                result.Status = "Fail";
                result.Message = ex.Message.ToString();
                result.ExMessage = ex.ToString();
                return result;
            }
            finally
            {
                if (isNewConnection && conn != null)
                {
                    conn.Close();
                }
            }
