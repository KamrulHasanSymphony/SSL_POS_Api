        public async Task<ResultVM> Insert(WithdrawalVM withdrawal)
        {
            string CodeGroup = "Withdrawal";
            string CodeName = "Withdrawal";
            CommonRepository _commonRepo = new CommonRepository();
            WithdrawalRepository _repo = new WithdrawalRepository();

            ResultVM result = new ResultVM { Status = "Fail", Message = "Error", ExMessage = null, Id = "0", DataVM = null };

            bool isNewConnection = false;
            SqlConnection conn = null;
            SqlTransaction transaction = null;
            try
            {
                conn = new SqlConnection(DatabaseHelper.GetConnectionString());
                conn.Open();
                isNewConnection = true;

                transaction = conn.BeginTransaction();

                

                string code = _commonRepo.CodeGenerationNo(CodeGroup, CodeName, conn, transaction);

                if (!string.IsNullOrEmpty(code))
                {
                    withdrawal.Code = code;
                }
                else
                {
                    throw new Exception("Code Generation Failed!");
                }
                #region Check Exist Data
                string[] conditionField = { "Code" };
                string[] conditionValue = { withdrawal.Code.Trim() };

                bool exist = _commonRepo.CheckExists("Withdrawals", conditionField, conditionValue, conn, transaction);

                if (exist)
                {
                    result.Message = "Data Already Exist!";
                    throw new Exception("Data Already Exist!");
                }
                #endregion

                result = await _repo.Insert(withdrawal, conn, transaction);

                if (isNewConnection)
                {
                    transaction.Commit();
                }

                return result;
            }
            catch (Exception ex)
            {
                if (transaction != null && isNewConnection)
                {
                    transaction.Rollback();
                }

                result.ExMessage = ex.ToString();
                return result;
            }
            finally
            {
                if (isNewConnection && conn != null)
                {
                    conn.Close();
                }
            }
        }

