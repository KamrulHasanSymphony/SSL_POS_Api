        public async Task<ResultVM> Insert(SalePersonRouteVM salePersonRoute)
        {
            string CodeGroup = "SalePersonRoute";
            string CodeName = "SalePersonRoute";
            CommonRepository _commonRepo = new CommonRepository();
            SalePersonRouteRepository _repo = new SalePersonRouteRepository();

            ResultVM result = new ResultVM { Status = "Fail", Message = "Error", ExMessage = null, Id = "0", DataVM = null };

            bool isNewConnection = false;
            SqlConnection conn = null;
            SqlTransaction transaction = null;
            try
            {
                conn = new SqlConnection(DatabaseHelper.GetConnectionString());
                conn.Open();
                isNewConnection = true;

                transaction = conn.BeginTransaction();

                var isRouteAssigned = await _repo.IsRouteAlreadyAssigned(salePersonRoute, conn, transaction);

                if (isRouteAssigned)
                {
                    result.Status = "Fail";
                    result.Message = "This route is already assigned to a salesperson!";
                    return result;
                }

                result = await _repo.Insert(salePersonRoute, conn, transaction);

                if (isNewConnection && result.Status == "Success")
                {
                    transaction.Commit();
                }
                else
                {
                    throw new Exception(result.Message);
                }

                return result;
            }
            catch (Exception ex)
            {
                if (transaction != null && isNewConnection)
                {
                    transaction.Rollback();
                }

                result.ExMessage = ex.ToString();
                return result;
            }
            finally
            {
                if (isNewConnection && conn != null)
                {
                    conn.Close();
                }
            }
        }

